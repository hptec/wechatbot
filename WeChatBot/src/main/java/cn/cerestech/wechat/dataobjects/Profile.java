package cn.cerestech.wechat.dataobjects;

public class Profile {
	private String Alias;
	private BufferedString BindEmail;
	private BufferedString BindMobile;
	private Long BindUin;
	private Integer BitFlag;
	private Integer HeadImgUpdateFlag;
	private String HeadImgUrl;
	private BufferedString NickName;
	private Integer PersonalCard;
	private Integer Sex;
	private String Signature;
	private Integer Status;
	private BufferedString UserName;
	public String getAlias() {
		return Alias;
	}
	public void setAlias(String alias) {
		Alias = alias;
	}
	public BufferedString getBindEmail() {
		return BindEmail;
	}
	public void setBindEmail(BufferedString bindEmail) {
		BindEmail = bindEmail;
	}
	public BufferedString getBindMobile() {
		return BindMobile;
	}
	public void setBindMobile(BufferedString bindMobile) {
		BindMobile = bindMobile;
	}
	public Long getBindUin() {
		return BindUin;
	}
	public void setBindUin(Long bindUin) {
		BindUin = bindUin;
	}
	public Integer getBitFlag() {
		return BitFlag;
	}
	public void setBitFlag(Integer bitFlag) {
		BitFlag = bitFlag;
	}
	public Integer getHeadImgUpdateFlag() {
		return HeadImgUpdateFlag;
	}
	public void setHeadImgUpdateFlag(Integer headImgUpdateFlag) {
		HeadImgUpdateFlag = headImgUpdateFlag;
	}
	public String getHeadImgUrl() {
		return HeadImgUrl;
	}
	public void setHeadImgUrl(String headImgUrl) {
		HeadImgUrl = headImgUrl;
	}
	public BufferedString getNickName() {
		return NickName;
	}
	public void setNickName(BufferedString nickName) {
		NickName = nickName;
	}
	public Integer getPersonalCard() {
		return PersonalCard;
	}
	public void setPersonalCard(Integer personalCard) {
		PersonalCard = personalCard;
	}
	public Integer getSex() {
		return Sex;
	}
	public void setSex(Integer sex) {
		Sex = sex;
	}
	public String getSignature() {
		return Signature;
	}
	public void setSignature(String signature) {
		Signature = signature;
	}
	public Integer getStatus() {
		return Status;
	}
	public void setStatus(Integer status) {
		Status = status;
	}
	public BufferedString getUserName() {
		return UserName;
	}
	public void setUserName(BufferedString userName) {
		UserName = userName;
	}
	
}
