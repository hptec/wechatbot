package cn.cerestech.wechat.dataobjects;

public class RecommendInfo {
	private String Alias;
	private Integer AttrStatus;
	private String City;
	private String Content;
	private String NickName;
	private Integer OpCode;
	private String Province;
	private Long QQNum;
	private Integer Scene;
	private Integer Sex;
	private String Signature;
	private String Ticket;
	private String UserName;
	private Integer VerifyFlag;

	public String getAlias() {
		return Alias;
	}

	public void setAlias(String alias) {
		Alias = alias;
	}

	public Integer getAttrStatus() {
		return AttrStatus;
	}

	public void setAttrStatus(Integer attrStatus) {
		AttrStatus = attrStatus;
	}

	public String getCity() {
		return City;
	}

	public void setCity(String city) {
		City = city;
	}

	public String getContent() {
		return Content;
	}

	public void setContent(String content) {
		Content = content;
	}

	public String getNickName() {
		return NickName;
	}

	public void setNickName(String nickName) {
		NickName = nickName;
	}

	public Integer getOpCode() {
		return OpCode;
	}

	public void setOpCode(Integer opCode) {
		OpCode = opCode;
	}

	public String getProvince() {
		return Province;
	}

	public void setProvince(String province) {
		Province = province;
	}

	public Long getQQNum() {
		return QQNum;
	}

	public void setQQNum(Long qQNum) {
		QQNum = qQNum;
	}

	public Integer getScene() {
		return Scene;
	}

	public void setScene(Integer scene) {
		Scene = scene;
	}

	public Integer getSex() {
		return Sex;
	}

	public void setSex(Integer sex) {
		Sex = sex;
	}

	public String getSignature() {
		return Signature;
	}

	public void setSignature(String signature) {
		Signature = signature;
	}

	public String getTicket() {
		return Ticket;
	}

	public void setTicket(String ticket) {
		Ticket = ticket;
	}

	public String getUserName() {
		return UserName;
	}

	public void setUserName(String userName) {
		UserName = userName;
	}

	public Integer getVerifyFlag() {
		return VerifyFlag;
	}

	public void setVerifyFlag(Integer verifyFlag) {
		VerifyFlag = verifyFlag;
	}

}
