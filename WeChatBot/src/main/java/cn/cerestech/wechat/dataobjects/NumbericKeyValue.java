package cn.cerestech.wechat.dataobjects;

public class NumbericKeyValue {

	private Integer Key;
	private Long Val;

	public Integer getKey() {
		return Key;
	}

	public void setKey(Integer key) {
		Key = key;
	}

	public Long getVal() {
		return Val;
	}

	public void setVal(Long val) {
		Val = val;
	}

}
